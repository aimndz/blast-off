function performSearch(query) {
  const searchQuery = query.toLowerCase();

  const filteredItems = items.filter(
    (item) =>
      item.name.toLowerCase().includes(searchQuery) ||
      item.category.toLowerCase().includes(searchQuery) ||
      item.description.toLowerCase().includes(searchQuery)
  );

  let searchResultsHTML;
  const searchTitle = document.getElementById("search-word");

  if (filteredItems.length > 0) {
    searchTitle.textContent = `"${searchQuery}"`;

    searchResultsHTML = filteredItems
      .map((item) => getItemCardHTML(item))
      .join("");

    const searchResultsContainer = document.getElementById("search-results");
    searchResultsContainer.innerHTML = searchResultsHTML;

    const searchWordContainer = document.querySelector(
      ".search-word-container"
    );

    const itemCount = searchResultsContainer.children.length;
    let itemCountEl = document.createElement("p");

    if (itemCount === 1) {
      itemCountEl.textContent = `( ${itemCount} item )`;
      searchWordContainer.appendChild(itemCountEl);
    } else {
      itemCountEl.textContent = `( ${itemCount} items )`;
      searchWordContainer.appendChild(itemCountEl);
    }
  } else {
    searchTitle.textContent = `No items found for "${query}"`;
  }
}

